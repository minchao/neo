Neo.translate = function () {
    var lang = "en";
    for (var key in Neo.dictionary) {
	if (navigator.language.indexOf(key) == 0) {
	    lang = key;
	    break;
	}
    }
    
    return function(string) {
        if (Neo.config.neo_alt_translation) {
            if (lang == "en") lang = "enx"
        } else {
            if (lang != "ja") lang = "en"
        }
	return Neo.dictionary[lang][string] || string;
    }
}();

